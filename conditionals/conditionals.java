package conditionals;
public class conditionals {
    public static void main(String[] args) 
    {
        
        int x = 1;

        while(x<6)//condition
        {
            System.out.println("Larry is good at tennis");
            x += 1;
        }
        
        int i = 1;
        while(i<=5)// increment by 1
        {
            System.out.println("Larry plays Tennis");
            i++;
        }
        for(int j=1; j<=5; j++) //for loop
        {
            System.out.println("Mr.Liu sucks");
        }
        
        for(int k = 2; k<=100; k+=2)// increment by 2
        {
            System.out.println(k);
        }
    }
}
